        boolean Bfound=false;
         for(int i=0;i<Book.getNumBooks();i++)//book valid check
           if(bookCallno.equals(getB()[i].getCallno())&&Integer.parseInt(getB()[i].getQuantity())>getB()[i].getIssued()){Bfound=true;break;}

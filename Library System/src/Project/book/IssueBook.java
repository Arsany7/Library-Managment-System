    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            BookRW.ReadBookFile();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
       StudentRW.ReadStdFile();
    boolean empty=false;
        String bookCallno=this.bookCallno.getText();
        String stdName=this.stdName.getText();
        String stdID=this.stdID.getText();
        String stdContact=this.stdContact.getText();
        DateTimeFormatter df = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate DA =LocalDate.now();
       String dateIssued =df.format(DA);
       
        //Date d1 = dateFormat.parse(getlasttoken);
         if(isNull(bookCallno)||isNull(stdName)||isNull(stdID)||isNull(stdContact))
         empty=true;
         if(!empty&&Checkers.isIntid(stdID)&&Checkers.ContactNoValidation(stdContact)){
         if(checkStd(stdID)&&checkBook(bookCallno)){
            try {
                BookRW.ReadIBookFile();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
            StudentRW.ReadStdFile();
             BookRW.WriteIssuedBook(bookCallno, false);
             BookRW.WriteIssuedBook(stdID, false);
             BookRW.WriteIssuedBook(stdName, false);
             BookRW.WriteIssuedBook(stdContact, false);
             BookRW.WriteIssuedBook(dateIssued,true);
             JOptionPane.showMessageDialog(this, "Book Issued !");
             for(int i=0;i<Student.getNum();i++){
                if (getS()[i].getId()==(Integer.parseInt(stdID))){
                     getS()[i].setnBooks(getS()[i].getnBooks()+1);
                     StudentRW.WriteStdARR();
                }
            }
             
             
             
             
             for(int i=0;i<Book.getNumBooks();i++){
                 if (getB()[i].getCallno().equals(bookCallno)) {
                     getB()[i].setIssued(getB()[i].getIssued() + 1);
                    BookRW.WriteBookARR();
                 }
             }
        }
        else if (checkBook(bookCallno) == false && checkStd(stdID) == false) {
            JOptionPane.showMessageDialog(this, "Book and Student are not found");
        } else if (checkBook(bookCallno) == false) {
            JOptionPane.showMessageDialog(this, "Book is not available");
        } else {
            JOptionPane.showMessageDialog(this, "Student not found/Limit exceeded");
        }
         }
    }//GEN-LAST:event_jButton1ActionPerformed

    
           
                    
